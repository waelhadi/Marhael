import marshal

def فك_التشفير(البيانات_المشفرة):
    # فك التشفير حتى الوصول إلى الكود الأصلي
    while isinstance(البيانات_المشفرة, bytes):
        البيانات_المشفرة = marshal.loads(البيانات_المشفرة)
    return البيانات_المشفرة

if __name__ == "__main__":
    print("=== دالة فك التشفير ===")
    البيانات_المشفرة = input("أدخل البيانات المشفرة: ").strip()
    try:
        الكود_الأصلي = فك_التشفير(eval(البيانات_المشفرة))
        exec(الكود_الأصلي)
    except Exception as e:
        print(f"خطأ أثناء فك التشفير: {str(e)}")
